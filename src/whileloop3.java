public class whileloop3 {
    public static void main(String[] args) {
        int t=10;
        while (t>0){
            System.out.println(t);
            t--;}
        System.out.println("******************");
        int w=1;
        int sum=0;
        while (w<=5){
            sum=sum+w;
            w++;

        }
        System.out.println(sum);


    }
}
